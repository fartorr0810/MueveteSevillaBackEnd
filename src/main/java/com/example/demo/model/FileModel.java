package com.example.demo.model;

public class FileModel {
	
	private String file;
	
	public FileModel() {
		super();
	}

	public FileModel( String file) {
		super();
		this.file = file;
	}

	public String getFile() {
		return file;
	}

	public void setFile(String file) {
		this.file = file;
	}
	
	
}
